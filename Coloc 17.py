"""Знайти суму елементів масиву дійсних чисел, що мають непарні номери.
Розмірність масиву - 20. Заповнення масиву здійснити випадковими числами від 100
до 200.
Мельник Д.В. 122А
"""
import random

massive = [random.uniform(100, 200) for i in range(20)]
summa = 0
print(massive)
for i in range(len(massive)):  # Проходим по елементам масиву
    if i % 2 != 0:  # Якщо остача при діленні на 2  не буде 0 то число непарне
        summa += massive[i]
print(f'Сума елементів непарних номерів: {summa}')