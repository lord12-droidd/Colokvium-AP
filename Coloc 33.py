"""Заданий масив А (1:20). Знайти добуток всіх його ненульових елементів.
Мельник Д.В. 122А
"""
import random

massive = [random.randint(-2, 2) for i in range(20)]  # Створюємо масив генератором списку
print(massive)
mult = 1
for i in massive:  # Проходим по елементам масиву
    if i != 0:  # Перевіряємо нашу умову
        mult *= i  # Шукаємо добуток таких чисел
print(f'Добуток ненульових елементів масиву: {mult}')
